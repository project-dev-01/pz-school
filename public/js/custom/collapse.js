
// First section
$('#collapseButton1').click(function () {
    var icon = $(this).find('i');
    icon.toggleClass('mdi-chevron-up mdi-chevron-down');
    var collapseContent = $(this).closest('.card').find('.collapse');
    collapseContent.toggle('show');
});

